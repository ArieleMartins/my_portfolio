export function showProjects(list){
    const container = document.getElementById('projetos-projeto');
    
    container.innerHTML = ``

    list.forEach(project =>{
        container.innerHTML += `
        <div class="container-projeto slide">          
            <div class="container-imagem">
                <figure>
                    <img src="${project.img}"
                        alt="${project.alt}">
                </figure>
            </div>
            <div class="container-desc-links">
                <div class="tecnologias">
                    <ul>
                        ${createLanguageProject(project)}
                    </ul>
                </div>
                <div class="desc">
                    <h3 class="nome-projeto">${project.desc.name}</h3>
                    <p>${project.desc.desc}</p>
                </div>
                <div class="container-links">
                    <div class="container-play">
                    <a href="${project.href}"
                        target="_blank" class="play"><i class="fa-solid fa-play"></i></a>
                    </div>
                    <div class="container-github">
                        <a href="${project.github}"
                        target="_blank" class="repo-github"><i class="fa-brands fa-github"></i></a>
                    </div>
                </div>
            </div>
        </div>          
        `
        })
    
    const slides = document.querySelectorAll('.slide')
    slides[0].style.display = 'flex'
}


function createLanguageProject(project){
    li=''
    project.tec.forEach(language =>{
        li += `
        <li class="hover width-${language.toLowerCase()}">
            <div class="tec-${language.toLowerCase()}"></div><span class="span hover-${language.toLowerCase()}">${language}</span>
        </li>
        `
    })
    
    return li
}
